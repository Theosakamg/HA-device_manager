def test_registry_create_auto_generates_id():
    """async_create auto-generates floor_id from the name (no external setter)."""
    registry = _FakeRegistry()
    entry = registry.async_create("Ground Floor", level=0)
    assert entry.floor_id == "ground_floor"
    assert entry.name == "Ground Floor"
    assert entry.level == 0


def test_registry_get_floor_by_name_found():
    """async_get_floor_by_name returns the correct entry."""
    registry = _FakeRegistry()
    registry.async_create("Level 1", level=1)
    result = registry.async_get_floor_by_name("Level 1")
    assert result is not None
    assert result.floor_id == "level_1"


def test_registry_get_floor_by_name_missing():
    """async_get_floor_by_name returns None when the name is not found."""
    registry = _FakeRegistry()
    assert registry.async_get_floor_by_name("Does Not Exist") is None


def test_rollback_deletes_created_floors():
    """When a 'create' action is rolled back, the floor must be deleted."""
    registry = _FakeRegistry()
    entry = registry.async_create("Ground", level=0)

    view = HaFloorsSyncAPIView.__new__(HaFloorsSyncAPIView)
    applied = [("create", entry.floor_id, None)]
    view._rollback(registry, applied)

    assert entry.floor_id in registry.deleted


def test_rollback_restores_updated_floors():
    """When an 'update' action is rolled back, the original values are restored."""
    registry = _FakeRegistry()
    entry = registry.async_create("Level 1", level=3)
    original = _FakeFloorEntry(entry.floor_id, "Level 1", 99)
    registry.async_update(entry.floor_id, name="Level 1", level=1)

    view = HaFloorsSyncAPIView.__new__(HaFloorsSyncAPIView)
    applied = [("update", entry.floor_id, original)]
    view._rollback(registry, applied)

    assert any(
        fid == entry.floor_id and name == "Level 1" and level == 99
        for fid, name, level in registry.updated
    )


def test_rollback_is_reversed_order():
    """Rollback applies changes in reverse order (last-in first-out)."""
    calls: list[str] = []

    class _TrackedRegistry(_FakeRegistry):
        def async_delete(self, floor_id):
            calls.append(f"delete:{floor_id}")
            super().async_delete(floor_id)

        def async_update(self, floor_id, *, name=None, level=None, **kw):
            calls.append(f"update:{floor_id}")
            super().async_update(floor_id, name=name, level=level)

    registry = _TrackedRegistry()
    e0 = registry.async_create("Ground", level=0)
    e1 = registry.async_create("Level 1", level=1)
    original_l1 = _FakeFloorEntry(e1.floor_id, "Level 1", 5)

    view = HaFloorsSyncAPIView.__new__(HaFloorsSyncAPIView)
    applied = [
        ("create", e0.floor_id, None),
        ("update", e1.floor_id, original_l1),
    ]
    view._rollback(registry, applied)

    # update (Level 1) must be processed before delete (Ground)
    assert calls == [f"update:{e1.floor_id}", f"delete:{e0.floor_id}"]


def test_rollback_skips_update_without_original():
    """A rollback for 'update' with no original snapshot does nothing."""
    calls: list[str] = []

    class _TrackedRegistry(_FakeRegistry):
        def async_update(self, floor_id, *, name=None, level=None, **kw):
            calls.append(f"update:{floor_id}")
            super().async_update(floor_id, name=name, level=level)

    view = HaFloorsSyncAPIView.__new__(HaFloorsSyncAPIView)
    applied = [("update", "some_floor", None)]  # no original snapshot
    view._rollback(_TrackedRegistry(), applied)

    assert calls == []


def test_rollback_tolerates_registry_error():
    """Rollback must not raise even if the registry raises on async_delete."""

    class _FailingRegistry:
        def async_delete(self, floor_id):
            raise RuntimeError("delete failed")

        def async_update(self, *a, **kw):
            pass

    view = HaFloorsSyncAPIView.__new__(HaFloorsSyncAPIView)
    view._rollback(_FailingRegistry(), [("create", "ground", None)])


