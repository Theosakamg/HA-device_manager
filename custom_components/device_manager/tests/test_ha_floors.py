# ---------------------------------------------------------------------------
# Registry stub that mirrors the real HA FloorRegistry API (HA 2024.4+)
#
# Real signatures (from homeassistant/helpers/floor_registry.py):
#   async_create(name, *, aliases=None, icon=None, level=None) -> FloorEntry
#   async_get_floor_by_name(name) -> FloorEntry | None
#   async_get_floor(floor_id) -> FloorEntry | None
#   async_update(floor_id, *, name=UNDEFINED, level=UNDEFINED, ...) -> FloorEntry
#   async_delete(floor_id) -> None
#
# floor_id is auto-generated by _generate_id(name) — callers cannot set it.
# ---------------------------------------------------------------------------


class _FakeFloorEntry:
    """Minimal stub of HA FloorEntry."""

    def __init__(self, floor_id: str, name: str, level):
        self.floor_id = floor_id
        self.name = name
        self.level = level


def _auto_id(name: str) -> str:
    """Mimic HA's _generate_id: slugify the name."""
    import re
    return re.sub(r"[^a-z0-9]+", "_", name.lower()).strip("_")


class _FakeRegistry:
    """Minimal stub of HA FloorRegistry for rollback testing."""

    def __init__(self):
        self.floors: dict[str, _FakeFloorEntry] = {}
        self.deleted: list[str] = []
        self.updated: list[tuple] = []

    def async_get_floor_by_name(self, name: str):
        for entry in self.floors.values():
            if entry.name == name:
                return entry
        return None

    def async_get_floor(self, floor_id: str):
        return self.floors.get(floor_id)

    def async_create(self, name: str, *, level=None, **_kw):
        """Auto-generate floor_id from name, exactly like HA."""
        floor_id = _auto_id(name)
        entry = _FakeFloorEntry(floor_id, name, level)
        self.floors[floor_id] = entry
        return entry

    def async_update(self, floor_id: str, *, name=None, level=None, **_kw):
        if floor_id in self.floors:
            if name is not None:
                self.floors[floor_id].name = name
            if level is not None:
                self.floors[floor_id].level = level
        self.updated.append((floor_id, name, level))

    def async_delete(self, floor_id: str):
        self.floors.pop(floor_id, None)
        self.deleted.append(floor_id)


