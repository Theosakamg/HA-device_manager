def test_ha_name_per_building_is_unique():
    """Floors in different buildings with the same name produce distinct HA names."""
    # "Home - Lvl0" vs "Erwan - Lvl0" must not collide
    home_l0 = "Home - Lvl0"
    erwan_l0 = "Erwan - Lvl0"
    assert home_l0 != erwan_l0


def test_level_resets_per_building():
    """Each building's floors are numbered from 0 independently.

    Home: Lvl0 → 0, Lvl1 → 1
    Erwan: Lvl0 → 0, Lvl1 → 1
    """
    registry = _FakeRegistry()
    # Simulate what the controller does: iterate buildings, then floors per building

    buildings = [("Home", ["Lvl0", "Lvl1"]), ("Erwan", ["Lvl0", "Lvl1"])]
    synced = []
    for building_name, floor_names in buildings:
        for idx, floor_name in enumerate(floor_names):
            ha_name = f"{building_name} - {floor_name}"
            entry = registry.async_create(ha_name, level=idx)
            synced.append({"name": ha_name, "level": idx})

    # Home floors
    assert synced[0] == {"name": "Home - Lvl0", "level": 0}
    assert synced[1] == {"name": "Home - Lvl1", "level": 1}
    # Erwan floors — level resets to 0
    assert synced[2] == {"name": "Erwan - Lvl0", "level": 0}
    assert synced[3] == {"name": "Erwan - Lvl1", "level": 1}
