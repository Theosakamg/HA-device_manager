def test_compute_area_id_basic():
    """_compute_area_id produces a stable slug from the full path."""
    area_id = _compute_area_id("Building A", "Floor 1", "Living Room")
    assert area_id == "building_a_floor_1_living_room"


def test_slugify_fallback_ascii():
    """_slugify regex fallback works for plain ASCII names."""
    assert _slugify("Hello World") == "hello_world"


def test_compute_area_id_special_chars():
    """_compute_area_id replaces special chars with underscores."""
    area_id = _compute_area_id("Maison", "RDC", "Cuisine")
    # Verify it's a valid slug (lowercase, no spaces)
    import re
    assert re.match(r"^[a-z0-9_]+$", area_id)


def test_registry_create_auto_generates_id():
    """async_create auto-generates area id from the name."""
    registry = _FakeAreaRegistry()
    entry = registry.async_create("Building A - Floor 1 - Living Room")
    assert entry.id == "building_a_floor_1_living_room"
    assert entry.name == "Building A - Floor 1 - Living Room"
    assert entry.floor_id is None


def test_registry_create_then_update_name_keeps_id():
    """After create+update, the area ID is stable and name reflects the update."""
    registry = _FakeAreaRegistry()
    entry = registry.async_create("Building A - Floor 1 - Kitchen", floor_id="floor_1")
    original_id = entry.id
    registry.async_update(original_id, name="Kitchen")
    # ID unchanged, display name updated
    updated = registry.async_get_area(original_id)
    assert updated is not None
    assert updated.id == original_id
    assert updated.name == "Kitchen"


def test_compute_area_id_matches_registry_create_id():
    """_compute_area_id must match the ID auto-generated by async_create."""
    registry = _FakeAreaRegistry()
    entry = registry.async_create("Maison - RDC - Bureau")
    computed = _compute_area_id("Maison", "RDC", "Bureau")
    assert entry.id == computed


def test_registry_create_with_floor_id():
    """async_create stores the floor_id."""
    registry = _FakeAreaRegistry()
    entry = registry.async_create("Building A - Floor 1 - Kitchen", floor_id="floor_1")
    assert entry.floor_id == "floor_1"


def test_registry_get_area_by_id_found():
    """async_get_area returns the correct entry when looked up by stable ID."""
    registry = _FakeAreaRegistry()
