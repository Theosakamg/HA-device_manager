# ---------------------------------------------------------------------------
# Area registry stub that mirrors the real HA AreaRegistry API
#
# Real signatures (from homeassistant/helpers/area_registry.py):
#   async_create(name, *, aliases=None, floor_id=None, icon=None, ...) -> AreaEntry
#   async_get_area_by_name(name) -> AreaEntry | None
#   async_get_area(area_id) -> AreaEntry | None
#   async_update(area_id, *, name=UNDEFINED, floor_id=UNDEFINED, ...) -> AreaEntry
#   async_delete(area_id) -> None
#
# area.id is auto-generated by _generate_id(name).
# ---------------------------------------------------------------------------


class _FakeAreaEntry:
    """Minimal stub of HA AreaEntry."""

    def __init__(self, area_id: str, name: str, floor_id: str | None = None):
        self.id = area_id
        self.name = name
        self.floor_id = floor_id


def _auto_id(name: str) -> str:
    """Mimic HA's _generate_id: slugify the name."""
    import re
    return re.sub(r"[^a-z0-9]+", "_", name.lower()).strip("_")


class _FakeAreaRegistry:
    """Minimal stub of HA AreaRegistry for rollback testing."""

    def __init__(self):
        self.areas: dict[str, _FakeAreaEntry] = {}
        self.deleted: list[str] = []
        self.updated: list[tuple] = []

    def async_get_area_by_name(self, name: str):
        for entry in self.areas.values():
            if entry.name == name:
                return entry
        return None

    def async_get_area(self, area_id: str):
        return self.areas.get(area_id)

    def async_create(self, name: str, *, floor_id=None, **_kw):
        """Auto-generate area id from name, exactly like HA."""
        area_id = _auto_id(name)
        entry = _FakeAreaEntry(area_id, name, floor_id)
        self.areas[area_id] = entry
        return entry

    def async_update(self, area_id: str, *, name=None, floor_id=None, **_kw):
        if area_id in self.areas:
            if name is not None:
                self.areas[area_id].name = name
            if floor_id is not None:
                self.areas[area_id].floor_id = floor_id
        self.updated.append((area_id, name, floor_id))

    def async_delete(self, area_id: str):
        self.areas.pop(area_id, None)
        self.deleted.append(area_id)


