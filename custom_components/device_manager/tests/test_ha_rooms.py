def test_duplicate_name_detection():
    """When two rooms share the same name, the set of duplicates is non-empty."""
    names = ["Bath", "Kitchen", "Bath", "Office"]
    duplicate_names = {n for n in names if names.count(n) > 1}
    assert "Bath" in duplicate_names
    assert "Kitchen" not in duplicate_names
    assert "Office" not in duplicate_names


def test_unique_name_not_marked_duplicate():
    """Rooms with unique names across all floors are not in the duplicate set."""
    names = ["Bath", "Kitchen", "Office", "Garden"]
    duplicate_names = {n for n in names if names.count(n) > 1}
    assert not duplicate_names


def test_rollback_deletes_created_areas():
    """When a 'create' action is rolled back, the area must be deleted."""
    registry = _FakeAreaRegistry()
    entry = registry.async_create("Building - Floor - Room A", floor_id="floor_1")

    view = HaRoomsSyncAPIView.__new__(HaRoomsSyncAPIView)
    applied = [("create", entry.id, None)]
    view._rollback(registry, applied)

    assert entry.id in registry.deleted
    assert entry.id not in registry.areas


def test_rollback_restores_updated_areas():
    """When an 'update' action is rolled back, the original values are restored."""
    registry = _FakeAreaRegistry()
    entry = registry.async_create("Building - Floor - Room B", floor_id="floor_old")
    original = _FakeAreaEntry(entry.id, "Building - Floor - Room B", "floor_old")
    registry.async_update(entry.id, name="Building - Floor - Room B", floor_id="floor_new")

    view = HaRoomsSyncAPIView.__new__(HaRoomsSyncAPIView)
    applied = [("update", entry.id, original)]
    view._rollback(registry, applied)

    assert any(
        aid == entry.id and fid == "floor_old"
        for aid, _name, fid in registry.updated
        if aid == entry.id
    )


def test_rollback_is_reversed_order():
    """Rollback applies changes in reverse order (last-in first-out)."""
    calls: list[str] = []

    class _TrackedRegistry(_FakeAreaRegistry):
        def async_delete(self, area_id):
            calls.append(f"delete:{area_id}")
            super().async_delete(area_id)

        def async_update(self, area_id, *, name=None, floor_id=None, **kw):
            calls.append(f"update:{area_id}")
            super().async_update(area_id, name=name, floor_id=floor_id)

    registry = _TrackedRegistry()
    e0 = registry.async_create("Building - Floor - Room C", floor_id="floor_1")
    e1 = registry.async_create("Building - Floor - Room D", floor_id="floor_1")
    original_d = _FakeAreaEntry(e1.id, "Building - Floor - Room D", "floor_old")

    view = HaRoomsSyncAPIView.__new__(HaRoomsSyncAPIView)
    applied = [
        ("create", e0.id, None),
        ("update", e1.id, original_d),
    ]
    view._rollback(registry, applied)

    # update (Room D) must be processed before delete (Room C)
    assert calls == [f"update:{e1.id}", f"delete:{e0.id}"]


def test_rollback_skips_update_without_original():
    """A rollback for 'update' with no original snapshot does nothing."""
    calls: list[str] = []

    class _TrackedRegistry(_FakeAreaRegistry):
        def async_update(self, area_id, *, name=None, floor_id=None, **kw):
            calls.append(f"update:{area_id}")
            super().async_update(area_id, name=name, floor_id=floor_id)

    view = HaRoomsSyncAPIView.__new__(HaRoomsSyncAPIView)
    applied = [("update", "some_area", None)]  # no original snapshot
    view._rollback(_TrackedRegistry(), applied)

    assert calls == []


def test_rollback_multiple_rooms_reversed():
    """Multiple creates are rolled back in reverse order."""
    calls: list[str] = []

    class _TrackedRegistry(_FakeAreaRegistry):
        def async_delete(self, area_id):
            calls.append(f"delete:{area_id}")
            super().async_delete(area_id)

    registry = _TrackedRegistry()
    e0 = registry.async_create("B - F - Room 0", floor_id="f0")
    e1 = registry.async_create("B - F - Room 1", floor_id="f0")
    e2 = registry.async_create("B - F - Room 2", floor_id="f0")

    view = HaRoomsSyncAPIView.__new__(HaRoomsSyncAPIView)
    applied = [
        ("create", e0.id, None),
        ("create", e1.id, None),
        ("create", e2.id, None),
    ]
    view._rollback(registry, applied)

    assert calls == [
        f"delete:{e2.id}",
        f"delete:{e1.id}",
        f"delete:{e0.id}",
    ]


